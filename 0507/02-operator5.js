//대입 연산자
console.log("=")
let a=10;
console.log("a=",a) //10

console.log("+=")
a+=5;
console.log("a+=5",a) //15

console.log("-=")
a-=3;
console.log("a-=3",a) //12

console.log("*=")
a*=2;
console.log("a*=2",a) //24

console.log("/=")
a/=4;
console.log("a/=4",a) //6

console.log("%=")
a%=4; 
console.log("a%=4",a)//2

console.log("**=")
a**=3; 
console.log("a**=3",a) // 제곱
